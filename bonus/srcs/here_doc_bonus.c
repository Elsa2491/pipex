int	ft_strcmp(char *argv, char *str)
{
        int     i;

        i = 0;
	if (!argv || !str)
		return (1);
        while (str[i] != '\0' && argv[i] != '\0' && argv[i] == str[i])
		i += 1;
        return (argv[i] - str[i]);
}

